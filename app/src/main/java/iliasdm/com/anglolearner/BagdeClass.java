package iliasdm.com.anglolearner;

public class BagdeClass {

    private String onfire;
    private String venividivici;
    private String conqueror;
    private String thunderbolt;
    private String wise;
    private String committed;

    public BagdeClass () {}

    public BagdeClass(String onfire, String venividivici, String conqueror, String thunderbolt, String wise, String committed) {
        this.onfire = onfire;
        this.venividivici = venividivici;
        this.conqueror = conqueror;
        this.thunderbolt = thunderbolt;
        this.wise = wise;
        this.committed = committed;
    }

    public String getOnfire() {
        return onfire;
    }

    public String getVenividivici() {
        return venividivici;
    }

    public String getConqueror() {
        return conqueror;
    }

    public String getThunderbolt() {
        return thunderbolt;
    }

    public String getWise() {
        return wise;
    }

    public String getCommitted() {
        return committed;
    }
}
